//*************************************************************************
// インクルード部
//*************************************************************************
#include "Result_ComposerName.h"

//*************************************************************************
// コンストラクタ
//*************************************************************************
Result_ComposerName::Result_ComposerName(wchar_t* NAME, int LENGTH)
{

	// 作曲者名をセット
	//**********************************************************************
	Name = NAME;

	// 作曲者名の文字数をセット
	//**********************************************************************
	nName_Length = LENGTH;

	// オブジェクトを何個作成するか？(最低 1〜)
	//**********************************************************************
	nObject_Num = nName_Length;

	// オブジェクトの個数分、必要なサイズを確保
	//**********************************************************************
	Font_Data = new FONT_DATA[nObject_Num];


	for (int i = 0; i < nObject_Num; i++)
	{
		// 座標(画像の中心を原点)
		//******************************************************************
		Font_Data[i].Obj_Pos = D3DXVECTOR2( 0.0f, 0.0f );

		// オブジェクトのサイズ(X,Y)
		//******************************************************************
		Font_Data[i].Obj_Size = D3DXVECTOR2(24.0f, 24.0f);

		// 画像のRGBA値
		//******************************************************************
		Font_Data[i].Red =
		Font_Data[i].Green =
		Font_Data[i].Brue =
		Font_Data[i].Alpha = 255;
	}

	// フォント描画の基準点を設定
	//******************************************************************
	Standard_Pos = D3DXVECTOR2(50.0f, 210.0f);

	CFont_Base::Init(24);

	// フォントテクスチャ作成
	//**********************************************************************
	CFont_Base::Create_Texture( Name, nObject_Num );

}

//*************************************************************************
// デストラクタ
//*************************************************************************
Result_ComposerName::~Result_ComposerName(void)
{

	CFont_Base::Finalise( nObject_Num );

}

//*************************************************************************
// 更新処理
//*************************************************************************
void Result_ComposerName::Update(void)
{

	CFont_Base::Update();
}

//*************************************************************************
// 描画処理
//*************************************************************************
void Result_ComposerName::Draw(void)
{
	for (int i = 0; i < nObject_Num; i++)
	{
		CFont_Base::Draw(i, i);
	}

}

