#include "Fade_Scene.h"

//***********************************************************************************************************
// コンストラクタ
//***********************************************************************************************************
Fade_Scene::Fade_Scene(void)
{
	// ここでオブジェクトのnew処理
	Background = new Fade_Background;
}

//***********************************************************************************************************
// デストラクタ
//***********************************************************************************************************
Fade_Scene::~Fade_Scene(void)
{
	// ここでオブジェクトのdelete処理
	delete Background;
}

//***********************************************************************************************************
// 更新
//***********************************************************************************************************
void Fade_Scene::Update(void)
{
	// ここでオブジェクトのupdate処理
	Background->Update();
}

//***********************************************************************************************************
// 描画
//***********************************************************************************************************
void Fade_Scene::Draw(void)
{
	// 描画順は上の行から順に(但し2Dベース画像はこの限りではありません)
	Background->Draw();
}