#include "CLight.h"


//***********************************************************************************************************
// 初期化処理
//***********************************************************************************************************
void CLight::Init(void)
{
	//ライト情報のクリア
	ZeroMemory(&Light, sizeof(D3DLIGHT9));

	Light.Type = D3DLIGHT_DIRECTIONAL;	// D3DLIGHT_DIRECTIONAL = 平行光源

	vecDir = D3DXVECTOR3(0.0f, -1.0f, 0.0f);

	//3次元ベクトルの正規化(長さを1にする)
	D3DXVec3Normalize((D3DXVECTOR3*)&Light.Direction, &vecDir);

	//ディフューズ(拡散反射光)
	Light.Diffuse = D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f);

	//アンビエント(環境光)
	Light.Ambient = D3DXCOLOR(0.5f, 0.5f, 0.5f, 0.5f);

	//ライトをレンダリングパイプラインに設定
	CDirectX::Get_pD3DDevice()->SetLight(0, &Light);	// 0 =ライトの番号(0以外もアリ)
														//ライトの設定
	CDirectX::Get_pD3DDevice()->LightEnable(0, TRUE);	// ○番ライトをTRUE(付ける)orFALSE(消す)
														//レンダーステートの設定
	CDirectX::Get_pD3DDevice()->SetRenderState(D3DRS_LIGHTING, TRUE);
}

