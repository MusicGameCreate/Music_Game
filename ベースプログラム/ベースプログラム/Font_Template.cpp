#include "Font_Template.h"
#include "CInput.h"
#include "Debug_DataSave.h"

//*************************************************************************
// コンストラクタ
//*************************************************************************
Font_Template::Font_Template(void)
{

	// オブジェクトを何個作成するか？(最低 1〜)
	//**********************************************************************
	nObject_Num = 20;


	// オブジェクトの個数分、必要なサイズを確保
	//**********************************************************************
	Font_Data = new FONT_DATA[nObject_Num];


	for ( int i = 0 ; i < nObject_Num ; i ++ )
	{
		// 座標(画像の中心を原点)
		//******************************************************************
		Font_Data[i].Obj_Pos = D3DXVECTOR2(0.0f, 0.0f);

		// オブジェクトのサイズ(X,Y)
		//******************************************************************
		Font_Data[i].Obj_Size = D3DXVECTOR2(24.0f, 24.0f);

		// 画像のRGBA値
		//******************************************************************
		Font_Data[i].Red =
		Font_Data[i].Green =
		Font_Data[i].Brue =
		Font_Data[i].Alpha = 255;
	}

	// フォント描画の基準点を設定(文字列最左上座標)
	//******************************************************************
	Standard_Pos = D3DXVECTOR2(50.0f, 435.0f);

	CFont_Base::Create_Texture(L"あいうえおかき", 7);

	CFont_Base::Init(24);
}

//*************************************************************************
// デストラクタ
//*************************************************************************
Font_Template::~Font_Template(void)
{

	CFont_Base::Finalise(1);

}

//*************************************************************************
// 更新処理
//*************************************************************************
void Font_Template::Update(void)
{
	CFont_Base::Update();
}

//*************************************************************************
// 描画処理
//*************************************************************************
void Font_Template::Draw(void)
{
	for (int i = 0; i < nObject_Num; i++)
	{
		CFont_Base::Draw(i, i);
	}

}

